import UIKit

// MARK: - Custom colors

extension UIColor {
    class func getCustomPinkColor() -> UIColor {
        UIColor(red: 0.92, green: 0.28, blue: 0.73, alpha: 1)
    }

    class func getCustomBlueColor() -> UIColor {
        UIColor(red: 0.25, green: 0.27, blue: 0.6, alpha: 1)
    }
    
    class func getCustomDarkBlueColor() -> UIColor {
        UIColor(red: 0, green: 0.05, blue: 0.30, alpha: 1)
    }
}
